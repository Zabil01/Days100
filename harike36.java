package days100;

public class harike36 {
    public static void main(String[] args) {
        
        System.out.println("Ini adalah program Java operasi aritmatika");

        // Mendeklarasikan dan menginisialisasi variabel
        int angka1 = 10;
        int angka2 = 10;

        // Melakukan operasi aritmatika
        int hasilPenjumlahan = angka1 + angka2;
        int hasilPerkalian = angka1 * angka2;
        

        // Menampilkan hasil operasi aritmatika
        System.out.println("Hasil penjumlahan " + angka1 + " + " + angka2 + " adalah " + hasilPenjumlahan);
        System.out.println("Hasil perkalian " + angka1 + " + " + angka2 + " adalah " + hasilPerkalian);
    }
}
