package days100;

public class harike34 {
    public static void main(String[] args) {
        // Menghitung jumlah bilangan bulat dari 1 hingga 10
        System.out.println("Menghitung jumlah bilangan bulat dari 1 hingga 10");

        // Inisialisasi variabel jumlah dengan nilai awal 0
        int jumlah = 0;

        // Loop for untuk menghitung jumlah bilangan bulat
        for (int i = 1; i <= 10; i++) {
            jumlah += i; // Menambahkan nilai i ke variabel jumlah
        }

        // Menampilkan hasil
        System.out.println("Jumlah: " + jumlah);
    }
}