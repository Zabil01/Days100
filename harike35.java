package days100;

public class harike35 {
    public static void main(String[] args) {
        
        System.out.println("program Java dengan penggunaan loop for:");

        
        System.out.println("Menghitung hasil pangkat dua dari angka 5 hingga 10:");
        for (int i = 5; i <= 10; i++) {
            
            int hasilPangkatDua = i * i;

            
            System.out.println("Angka " + i + " pangkat dua adalah " + hasilPangkatDua);
        }
    }
}